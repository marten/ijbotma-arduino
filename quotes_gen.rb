#!/usr/bin/ruby

require 'csv'

def c_string(s)
  s = s.gsub(/\n/, '\\\\n').gsub(/"/, '\\"')
  return '"' + s + '"'
end

puts '// AUTOGENERATED. DO NOT EDIT. INVOKE \'make\' TO REGENERATE.'
puts ''
puts '#include "quotes.h"'
puts ''
puts '#include <avr/pgmspace.h>'
puts ''

count = 0
STDIN.each do |line|
  line.chomp!
  puts "static char const QUOTE_#{count}[] PROGMEM = #{c_string line};"
  count += 1
end
puts ''

print 'char const *const QUOTES[] PROGMEM = { '
count.times do |i|
  print "QUOTE_#{i}, "
end
puts '};'
puts ''

puts "int const NUM_QUOTES = #{count};"
